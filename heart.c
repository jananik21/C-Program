#include<stdio.h>
int main()
{
int a,b,size,count=0,end1;
scanf("%d",&size);
for(a=size/2;a<=size;a=a+2)
{
    for(b=1;b<size-a;b=b+2)
    {
        for(b=1;b<size-a;b++)
        {
            count<<' ';
          {
              for(b=1;b<size-a;b++)
              {
                  count<<'n';
                  {
                      for(b=1;b<size-a;b++)
                      {

                          count<<' ';
                          {
                              for(b=1;b<a-1;b++)
                              {
                                  count<<'A';
                                  {
                                      count<<end1;
                                      printf("*");
                                  }
                              }
                          }
                      }
                  }
              }
          }
        }
    }
}
}
